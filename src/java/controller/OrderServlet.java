    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
       // processRequest(request, response);
          User user = (User)request.getSession().getAttribute("account");
          CustomerDAO cd = new CustomerDAO();
          Customer c = cd.getCustomerById(user.getCustomerID());
          request.setAttribute("customer", c);
        
          
         String shoppingCartId = request.getParameter("shoppingcartid");
         try {
            int shoppingcartid = Integer.parseInt(shoppingCartId);
            CartDAO cad = new CartDAO();
            Cart ca = cad.getCartByShoppingCartId(shoppingcartid);
            request.setAttribute("ca", ca);
            
        } catch (NumberFormatException e) {
        }
         request.getRequestDispatcher("order.jsp").forward(request, response);
    }

